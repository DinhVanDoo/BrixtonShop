public class Collections {
    private int coId;
    private String coName;

    public Collections() {
    }

    public Collections(int coId, String coName) {
        this.coId = coId;
        this.coName = coName;
    }

    public int getCoId() {
        return coId;
    }

    public void setCoId(int coId) {
        this.coId = coId;
    }

    public String getCoName() {
        return coName;
    }

    public void setCoName(String coName) {
        this.coName = coName;
    }
    
    
    
    
}
