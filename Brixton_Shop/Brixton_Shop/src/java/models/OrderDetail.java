package models;

/**
 *
 * @author dinhd513
 */
public class OrderDetail {
    private int orId;
    private int proId;
    private int quantity;
    private float price;

    public OrderDetail() {
    }

    public OrderDetail(int orId, int proId, int quantity, float price) {
        this.orId = orId;
        this.proId = proId;
        this.quantity = quantity;
        this.price = price;
    }

    public int getOrId() {
        return orId;
    }

    public void setOrId(int orId) {
        this.orId = orId;
    }

    public int getProId() {
        return proId;
    }

    public void setProId(int proId) {
        this.proId = proId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
    
    
    
}
